# celcius to fahrenheit

#fahrenheit calculate

celcius = 37.5

fahrenheit = (celcius * 1.8 ) + 32

print(f"celcius {celcius} degree is equal to fahrenheit {fahrenheit} degree")

#fahrenheit to calcius

fahrenheit = 500

calcius = (fahrenheit - 32 )/ 1.8

print(f"fahrenheit {fahrenheit}  degree is equal to calcius {calcius} degree")
